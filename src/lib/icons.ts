  const size = opts.size ?? 24;
  const sw = opts.strokeWidth ?? 2;
  const cls = opts.className ? ` class="${opts.className}"` : '';
  const aria = opts.ariaLabel
    ? ` role="img" aria-label="${opts.ariaLabel}"`
    : ' aria-hidden="true"';
